DROP TABLE IF EXISTS film_genre;
DROP TABLE IF EXISTS genre;
DROP TABLE IF EXISTS film_like;
DROP TABLE IF EXISTS film;
DROP TABLE IF EXISTS rating;
DROP TABLE IF EXISTS friendship;
DROP TABLE IF EXISTS filmorate_user;

CREATE TABLE IF NOT EXISTS rating (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name VARCHAR (255)
);

CREATE TABLE IF NOT EXISTS film (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name VARCHAR (255),
  description VARCHAR (255),
  releaseDate TIMESTAMP,
  duration INTEGER,
  mpa INTEGER REFERENCES rating(id)
);

CREATE TABLE IF NOT EXISTS genre (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name VARCHAR (255)
);

CREATE TABLE IF NOT EXISTS film_genre (
  film_id INTEGER,
  genre_id INTEGER REFERENCES genre(id),
  PRIMARY KEY (film_id, genre_id)
);

CREATE TABLE IF NOT EXISTS filmorate_user (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  email VARCHAR (255),
  login VARCHAR (255),
  name VARCHAR (255),
  birthday TIMESTAMP
);

CREATE TABLE IF NOT EXISTS film_like (
  film_id INTEGER REFERENCES film(id),
  user_id INTEGER REFERENCES filmorate_user(id),
  PRIMARY KEY (film_id, user_id)
);

CREATE TABLE IF NOT EXISTS friendship (
  user_id INTEGER REFERENCES filmorate_user(id),
  friend_id INTEGER REFERENCES filmorate_user(id),
  PRIMARY KEY (user_id, friend_id)
);